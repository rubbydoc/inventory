    private void jComboBox4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox4ActionPerformed

        if (jComboBox4.getSelectedItem().toString().equals("Approve")) {
            int row = jTable2.getSelectedRow();
            String id = jTable2.getModel().getValueAt(row, 0).toString();
            String stat = jTable2.getModel().getValueAt(row, 6).toString();
            if (stat.equals("to be approved")) {
                try {

                    PreparedStatement ps = c.connect().prepareStatement("UPDATE users SET Status=? WHERE UserID=?");
                    ps.setString(1, "active");
                    ps.setString(2, id);

                    ps.executeUpdate();

                    c.connect().close();
                } catch (SQLException e) {
                    System.out.println(e);
                }
                selectUser();
            }
        }
        if (jComboBox4.getSelectedItem().toString().equals("Decline")) {

            int row = jTable2.getSelectedRow();
            String id = jTable2.getModel().getValueAt(row, 0).toString();
            String stat = jTable2.getModel().getValueAt(row, 6).toString();
            if (stat.equals("to be approved")) {
                try {

                    PreparedStatement ps = c.connect().prepareStatement("DELETE FROM users WHERE UserID=?");
                    ps.setString(1, id);

                    ps.executeUpdate();

                    c.connect().close();
                } catch (SQLException e) {
                    System.out.println(e);
                }
                selectUser();
            }
            refresh();
        }

    }//GEN-LAST:event_jComboBox4ActionPerformed

    public void selectUser() {
        try {

            Statement stmt = c.connect().createStatement();
            ResultSet rs = stmt.executeQuery("select * from users where Status != 'removed'");
            DefaultTableModel tm = (DefaultTableModel) jTable2.getModel();
            tm.setRowCount(0);

            while (rs.next()) {
                Object o[] = {rs.getInt(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6), rs.getString(7)};
                tm.addRow(o);

            }
            c.connect().close();
        } catch (SQLException e) {
            System.out.println(e);
        }

    }

